package com.yuen.xiuka.beans;

public class Shuzi {
    private int shuzi;
    private String code;
    private String msg;

    public int getShuzi() {
        return this.shuzi;
    }

    public void setShuzi(int shuzi) {
        this.shuzi = shuzi;
    }

    public String getCode() {
        return this.code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return this.msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
